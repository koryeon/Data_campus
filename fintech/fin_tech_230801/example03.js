function plus (p1,p2){
    console.log(p1+p2);
}

const plusArrowFunc = (p1,p2) =>{
    console.log(p1+p2);
};

const minusArrowFunc = (p1,p2) =>{
    console.log(p1-p2);
};

const multiArrowFunc = (p1,p2) =>{
    console.log(p1*p2);
};

const divArrowFunc = (p1,p2) =>{
    console.log(p1/p2);
};

plus(5,10);
plusArrowFunc(5,10);
minusArrowFunc(5,10);
multiArrowFunc(5,10);
divArrowFunc(5,10);
